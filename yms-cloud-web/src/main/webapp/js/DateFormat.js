Date.prototype.ylFormat = function (mask, utc) {
    return jsDateFormat(this, mask, utc);
};

// 转化为对应时区偏移值的时间
function toTheTimeZoneTime(offset) {
    var d = new Date();
    var utc = d.getTime() + (d.getTimezoneOffset() * 60000);
    var nd = new Date(utc + (3600000*offset));
    return nd;
}

function internationalFormatTime(date) {
    //时间格式国际化
    var language = window['getLan'] ? getLan() : 'en-US';
    if (language == 'en-US') {
        return date.ylFormat('ylEnglishTime');
    } else {
        return date.ylFormat("ylisoDateTime");
    }
}